package exemplos;

public class Array_loop02 {

	public static void main(String[] args) {
		
		// O exemplo a seguir gera todos os elementos do array cars , usando um loop " for-each ":
		
		 String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
		    for (String i : cars) {
		      System.out.println(i);
		    }    
	}

}
