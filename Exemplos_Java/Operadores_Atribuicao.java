package exemplos;

public class Operadores_Atribuicao {

	public static void main(String[] args) {
		
		// Operadores Atribuição
		
		// Exemplo atribuição
		int a = 5;
	    System.out.println(a);
	    
	    // Soma
	    int b = 5;
	    b += 3;
	    System.out.println("Soma: " + b);
	    
	    // Subtração
	    int c = 5;
	    c -= 3;
	    System.out.println("Subtração: " + c);
	    
	    // Multiplicação
	    int d = 5;
	    d *= 3;
	    System.out.println("Multiplicação: " + d);
	    
	    // Divisão 
	    double e = 5;
	    e /= 3;
	    System.out.println("Divisão: " + e);
	    
	    // Módulo
	    int f = 5;
	    f %= 3;
	    System.out.println("Módulo: " + f);
	    
	 // Exemplo de outros operadores
	    
	    int g = 5;
	    g &= 3;
	    System.out.println(g);
	    
	    int h = 5;
	    h |= 3;
	    System.out.println(h);

	    int i = 5;
	    i ^= 3;
	    System.out.println(i);
	    
	    int j = 5;
	    j >>= 3;
	    System.out.println(j);
	    
	    int x = 5;
	    x <<= 3;
	    System.out.println(x);
	}

}
