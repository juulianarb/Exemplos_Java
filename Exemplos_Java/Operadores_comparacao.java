package exemplos;

public class Operadores_comparacao {

	public static void main(String[] args) {
		
		// Exemplos de operadores de comparação
		
		// Exemplo 1: == igual
		
		int a = 5;
	    int b = 3;
	    System.out.println("Exemplo 1: " + (a == b)); // retorna falso porque 5 não é igual a 3
	    
	    // Exemplo 2: != diferente
	    
	    int c = 5;
	    int d = 3;
	    System.out.println("Exemplo 2: " + (c != d)); // retorna verdadeiro porque 5 não é igual a 3
	    
	    // Exemplo 3: > maior 
	    
	    int e = 5;
	    int f = 3;
	    System.out.println("Exemplo 3: " + (e > f)); // retorna verdadeiro porque 5 é maior que 3
	    
	    // Exemplo 4: < menor
	    
	    int g = 5;
	    int h = 3;
	    System.out.println("Exemplo 4: " + (g < h)); // retorna falso porque 5 não é menor que 3
	    
	    // Exemplo 5: >= maior ou igual
	    
	    int i = 5;
	    int j = 3;
	    System.out.println("Exemplo 5: " + (i >= j)); // retorna verdadeiro porque 5 é maior, ou igual, a 3
	    
	    // Exemplo 6: <= menor ou igual
	    
	    int x = 5;
	    int y = 3;
	    System.out.println("Exemplo 6: " + (x <= y)); // retorna falso porque 5 não é menor ou igual a 3
	    
	    

	}

}
