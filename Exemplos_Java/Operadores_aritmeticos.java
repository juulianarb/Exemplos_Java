package exemplos;

public class Operadores_aritmeticos {

	public static void main(String[] args) {
		
		// Operadores aritméticos
		
		// Adição 
		 int a = 5;
		 int b = 6;
		 System.out.println("Soma: " + a + b);
		 
		 // Subtração
		 int c = 5;
		 int d = 3;
		 System.out.println("Subtração: " + (c - d));
		 
		 // Multiplicação
		 int e = 5;
		 int f = 3;
		 System.out.println("Multiplicação: " + e * f);

		 // Divisão
		 int g = 12;
		 int h = 3;
		 System.out.println("Divisão: " + g / h);
		 
		 // Módulo - resto da divisão
		 int i = 5;
		 int j = 2;
		 System.out.println("Módulo: " + i % j);
		 
		 // Incremento
		 int k = 5;
		  ++k;
		    System.out.println("Incremento: " + k);
		    
		 // Decremento
		    int x = 5;
		    --x;
		    System.out.println("Decremento: " + x);
	}
	

}
